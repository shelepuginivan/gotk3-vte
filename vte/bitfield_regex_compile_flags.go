package vte

// RegexCompileFlags is a bitfield type that represents PCRE2 compilation
// flags.
//
// See man:pcre2api(3) for more information about every flag.
type RegexCompileFlags uint

const (
	REGEX_COMPILE_FLAGS_ANCHORED            RegexCompileFlags = 0x80000000
	REGEX_COMPILE_FLAGS_NO_UTF_CHECK        RegexCompileFlags = 0x40000000
	REGEX_COMPILE_FLAGS_ENDANCHORED         RegexCompileFlags = 0x20000000
	REGEX_COMPILE_FLAGS_ALLOW_EMPTY_CLASS   RegexCompileFlags = 0x00000001
	REGEX_COMPILE_FLAGS_ALT_BSUX            RegexCompileFlags = 0x00000002
	REGEX_COMPILE_FLAGS_AUTO_CALLOUT        RegexCompileFlags = 0x00000004
	REGEX_COMPILE_FLAGS_CASELESS            RegexCompileFlags = 0x00000008
	REGEX_COMPILE_FLAGS_DOLLAR_ENDONLY      RegexCompileFlags = 0x00000010
	REGEX_COMPILE_FLAGS_DOTALL              RegexCompileFlags = 0x00000020
	REGEX_COMPILE_FLAGS_DUPNAMES            RegexCompileFlags = 0x00000040
	REGEX_COMPILE_FLAGS_EXTENDED            RegexCompileFlags = 0x00000080
	REGEX_COMPILE_FLAGS_FIRSTLINE           RegexCompileFlags = 0x00000100
	REGEX_COMPILE_FLAGS_MATCH_UNSET_BACKREF RegexCompileFlags = 0x00000200
	REGEX_COMPILE_FLAGS_MULTILINE           RegexCompileFlags = 0x00000400
	REGEX_COMPILE_FLAGS_NEVER_UCP           RegexCompileFlags = 0x00000800
	REGEX_COMPILE_FLAGS_NEVER_UTF           RegexCompileFlags = 0x00001000
	REGEX_COMPILE_FLAGS_NO_AUTO_CAPTURE     RegexCompileFlags = 0x00002000
	REGEX_COMPILE_FLAGS_NO_AUTO_POSSESS     RegexCompileFlags = 0x00004000
	REGEX_COMPILE_FLAGS_NO_DOTSTAR_ANCHOR   RegexCompileFlags = 0x00008000
	REGEX_COMPILE_FLAGS_NO_START_OPTIMIZE   RegexCompileFlags = 0x00010000
	REGEX_COMPILE_FLAGS_UCP                 RegexCompileFlags = 0x00020000
	REGEX_COMPILE_FLAGS_UNGREEDY            RegexCompileFlags = 0x00040000
	REGEX_COMPILE_FLAGS_UTF                 RegexCompileFlags = 0x00080000
	REGEX_COMPILE_FLAGS_NEVER_BACKSLASH_C   RegexCompileFlags = 0x00100000
	REGEX_COMPILE_FLAGS_ALT_CIRCUMFLEX      RegexCompileFlags = 0x00200000
	REGEX_COMPILE_FLAGS_ALT_VERBNAMES       RegexCompileFlags = 0x00400000
	REGEX_COMPILE_FLAGS_USE_OFFSET_LIMIT    RegexCompileFlags = 0x00800000
	REGEX_COMPILE_FLAGS_EXTENDED_MORE       RegexCompileFlags = 0x01000000
	REGEX_COMPILE_FLAGS_LITERAL             RegexCompileFlags = 0x02000000
	REGEX_COMPILE_FLAGS_MATCH_INVALID_UTF   RegexCompileFlags = 0x04000000
	REGEX_COMPILE_FLAGS_ALT_EXTENDED_CLASS  RegexCompileFlags = 0x08000000
)
